package operator;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int A=1;
        int B=6;
        int C=8;

        System.out.println(A+B+C);
        System.out.println(A-B-C);
        System.out.println(A*B*C);
        System.out.println(A%B);
        System.out.println(C/B);
    }
}
