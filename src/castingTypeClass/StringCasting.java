package castingTypeClass;

public class StringCasting {
    public static void main(String[] args) {
        int a = 11;
        String.valueOf(a);
        System.out.println("This is Int to String " + a);

        char b = 'a';
        String.valueOf(b);
        System.out.println("This is Char to String " + b);
    }
}
