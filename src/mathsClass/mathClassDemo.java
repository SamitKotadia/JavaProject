package mathsClass;

public class mathClassDemo {
    public static void main(String[] args) {
        System.out.println("Maximum number "+Math.max(98.322,74.4343));
        System.out.println("Random Number "+ Math.random());
        System.out.println("This is square root "+Math.sqrt(64));
        System.out.println("Value of PI "+Math.PI);
        System.out.println("This is rounded value "+Math.ceil(20.89));
        System.out.println("This is minimum Value "+Math.min(.02,.002));
    }
}
